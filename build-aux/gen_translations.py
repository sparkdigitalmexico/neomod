#!/usr/bin/env python3
import re, subprocess, sys
from pathlib import Path

keywords = ["_", "tformat"]

# Add a new language here to automatically generate a new .po file!
languages = {
    "ru": "Русский",
    "fr": "Français",
    "pl": "Polski",
    "de": "Deutsch",
}

srcdir = sys.argv[1]
builddir = sys.argv[2]

SOURCES = subprocess.run(
    ["find", srcdir + "/src", "-name", "*.cpp", "-o", "-name", "*.h"],
    capture_output=True,
    text=True,
).stdout.splitlines()

# Regenerate template file to contain latest strings
Path(f"{srcdir}/translations/neomod.pot").unlink()
subprocess.run(
    ["xgettext"]
    + ["--keyword=" + k for k in keywords]
    + [
        "--add-comments=TRANSLATORS",
        "--sort-by-file",
        "-o",
        f"{srcdir}/translations/neomod.pot",
        "--package-name=neomod",
    ]
    + SOURCES
)

# Generate or update language files to match latest languages/strings list
for code, lang in languages.items():
    po = Path(f"{srcdir}/translations/{code}.po")
    if po.exists():
        subprocess.run(
            [
                "msgmerge",
                "-U",
                f"{srcdir}/translations/{code}.po",
                f"{srcdir}/translations/neomod.pot",
            ]
        )
    else:
        print(f"{code}.po doesn't exist, generating a new file.")
        subprocess.run(
            [
                "msginit",
                "--no-translator",
                "-i",
                f"{srcdir}/translations/neomod.pot",
                "-o",
                f"{srcdir}/translations/{code}.po",
                "-l",
                code,
            ]
        )

    subprocess.run(
        [
            "msgfmt",
            "-o",
            f"{builddir}/gen/{code}.mo",
            f"{srcdir}/translations/{code}.po",
        ]
    )

# Get list of strings present in SOURCES files
# Similar to xgettext, but for usage in our codebase instead
strings = {}
pattern = re.compile(
    r"\b(?:" + "|".join(keywords) + r')\s*\(\s*"((?:[^"\\]|\\.)*)"\s*[\)\,]'
)
for f in SOURCES:
    for m in pattern.finditer(Path(f).read_text(errors="replace")):
        strings[m.group(1)] = True

# Write translations.h
# Contains a TRANSLATABLE_STRINGS array and a LANGUAGES array, see i18n.cpp for their use
out = """// Auto-generated by gen_translations.py - do not edit
#pragma once
#include <array>
#include <string_view>

using namespace std::string_view_literals;

inline constexpr auto TRANSLATABLE_STRINGS = std::to_array<std::string_view>({
"""
for s in sorted(strings):
    out += f'    "{s}"sv,\n'
out += """});

inline constexpr auto LANGUAGES = std::to_array<std::pair<std::string_view, std::string_view>>({
    {"en"sv, "English"sv},
"""
for code, lang in languages.items():
    out += f'    {{"{code}"sv, "{lang}"sv}},\n'
out += "});\n\n"

Path(f"{builddir}/gen/translations.h").write_text(out)

# Write locale_embed.manifest
# With a hack to embed builddir stuff when manifest is expected to look at srcdir
hackdir = Path(builddir).resolve().relative_to(Path(srcdir).resolve())
Path(f"{builddir}/gen/locale_embed.manifest").write_text(
    "\n".join([f"locale_{code} : {hackdir}/gen/{code}.mo" for code in languages])
)
